# list, ordered
employees = ["Dhivya", "Lo Zhi", "Saifudin"]
print(employees[1])

# dictionary, ordered
person = {"name": "john doe", "age": 45}
print(f"{person['name']} {person['age']}")

# tuple, ordered
staff = ("Abu", "Ravi", "Ah Chong")
print(staff[0])

# set - can't have same values, no order
fruits = {"kiwi", "apple", "grapes"}
print(fruits)

